package com.cn.philips.pojo;

public class G11Matrix {
    private Integer id;

    private Double xg11;

    private Double yg11;

    private Double wg11;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Double getXg11() {
        return xg11;
    }

    public void setXg11(Double xg11) {
        this.xg11 = xg11;
    }

    public Double getYg11() {
        return yg11;
    }

    public void setYg11(Double yg11) {
        this.yg11 = yg11;
    }

    public Double getWg11() {
        return wg11;
    }

    public void setWg11(Double wg11) {
        this.wg11 = wg11;
    }
}