package com.cn.philips.pojo;

public class G12Matrix {
    private Integer id;

    private Double xg12;

    private Double yg12;

    private Double wg12;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Double getXg12() {
        return xg12;
    }

    public void setXg12(Double xg12) {
        this.xg12 = xg12;
    }

    public Double getYg12() {
        return yg12;
    }

    public void setYg12(Double yg12) {
        this.yg12 = yg12;
    }

    public Double getWg12() {
        return wg12;
    }

    public void setWg12(Double wg12) {
        this.wg12 = wg12;
    }
}