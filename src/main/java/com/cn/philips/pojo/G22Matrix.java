package com.cn.philips.pojo;

public class G22Matrix {
    private Integer id;

    private Double xg22;

    private Double yg22;

    private Double wg22;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Double getXg22() {
        return xg22;
    }

    public void setXg22(Double xg22) {
        this.xg22 = xg22;
    }

    public Double getYg22() {
        return yg22;
    }

    public void setYg22(Double yg22) {
        this.yg22 = yg22;
    }

    public Double getWg22() {
        return wg22;
    }

    public void setWg22(Double wg22) {
        this.wg22 = wg22;
    }
}